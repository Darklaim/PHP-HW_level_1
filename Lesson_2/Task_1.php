<?php
$a = rand(-10, 10);
$b = rand(-10, 10);
echo 'a: '.$a.'<br>';
echo 'b: '.$b.'<br>';
if ($a >= 0 && $b >= 0) {
    echo 'a - b = '.($a - $b); //если $a и $b положительные, вывести их разность;
}else if ($a < 0 && $b < 0) {
    echo 'a * b = '.($a * $b); // если $а и $b отрицательные, вывести их произведение;
}else {
    echo 'a + b = '.($a + $b); // если $а и $b разных знаков, вывести их сумму;
}